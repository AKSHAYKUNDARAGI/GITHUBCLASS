public class assignmentoperators {
    public static void main(String[] args) {
        int number = 10;

        //adding
        number += 2;
        System.out.println("After addition: num = " + number);

        // subtracting
        number -= 4;
        System.out.println("After subtraction: num = " + number);

        // multiplying
        number *= 7;
        System.out.println("After multiplication: num = " + number);

        // dividing
        number /= 2;
        System.out.println("After division: num = " + number);

        // After modulus
        number %= 5;
        System.out.println("After modulus: num = " + number);
    }
}
